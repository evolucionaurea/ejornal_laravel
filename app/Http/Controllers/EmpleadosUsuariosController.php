	public function search(Request $request){

		$q_users = User::with(['especialidad','rol','cliente_actual'])
		->where('id_cliente_actual',auth()->user()->id_cliente_actual);

		if($request->search){
			$q_users->where('nombre','like',"%{$request->search}%");
		}
		$q_users->skip($request->start)->take($request->length);

		return response()->json([
			'user'=>auth()->user(),
			'result'=>$q_users->orderBy('nombre', 'asc')->get()
		]);
	}

